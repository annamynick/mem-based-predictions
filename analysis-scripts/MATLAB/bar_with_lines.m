function bar_with_lines(data)

nrows = size(data,1);
figure()
bar(nanmean(data), ...
    'FaceColor',([167 167 167]/255),'EdgeColor','k');
hold on
for r = 1:nrows
    plot(data(r,:), ...
        'Color','k', ...
        'MarkerEdgeColor','k', ...
        'MarkerFaceColor','white', ...
        'Marker','o', ...
        'MarkerSize',8)
end 


end 
